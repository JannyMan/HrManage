package com.whgghr.hrm.model.po;

import java.util.Date;

public class InviteJob {

	private int id;
	private String name;
	private String sex;
	private String age;
	private Date born;
	private String isstock;
	private String content;
	private Date createTime;
	private String address;
	private String tel;
	private String afterSchool;
	private String teachSchool;
	private String experience;
	private String specialty;
	private String job;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	public Date getBorn() {
		return born;
	}
	public void setBorn(Date born) {
		this.born = born;
	}
	public String getIsstock() {
		return isstock;
	}
	public void setIsstock(String isstock) {
		this.isstock = isstock;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public Date getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	public String getAfterSchool() {
		return afterSchool;
	}
	public void setAfterSchool(String afterSchool) {
		this.afterSchool = afterSchool;
	}
	public String getTeachSchool() {
		return teachSchool;
	}
	public void setTeachSchool(String teachSchool) {
		this.teachSchool = teachSchool;
	}
	public String getExperience() {
		return experience;
	}
	public void setExperience(String experience) {
		this.experience = experience;
	}
	public String getSpecialty() {
		return specialty;
	}
	public void setSpecialty(String specialty) {
		this.specialty = specialty;
	}
	public String getJob() {
		return job;
	}
	public void setJob(String job) {
		this.job = job;
	}
	
}
